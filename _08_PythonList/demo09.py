"""
Python3 List max()方法
描述
max() 方法返回列表元素中的最大值。
语法
max()方法语法：
max(list)
参数
list -- 要返回最大值的列表。
返回值
返回列表元素中的最大值。
"""
list1, list2 = ['Google', 'Runoob', 'Taobao'], [456, 700, 200]
print("list1 最大元素值 : ", max(list1))
print("list2 最大元素值 : ", max(list2))
