"""
Python3 List min()方法
描述
min() 方法返回列表元素中的最小值。

语法
min()方法语法：

min(list)
参数
list -- 要返回最小值的列表。
返回值
返回列表元素中的最小值。
"""

list1, list2 = ['Google', 'Runoob', 'Taobao'], [456, 700, 200]

print("list1 最小元素值 : ", min(list1))
print("list2 最小元素值 : ", min(list2))
