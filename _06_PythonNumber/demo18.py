"""
Python3 sqrt() 函数
描述：sqrt() 方法返回数字x的平方根
语法：import math math.sqrt(x)
参数：
x -- 数值表达式
返回值：返回数字x的平方根
"""
import math   # 导入 math 模块

print("math.sqrt(100) : ", math.sqrt(100))
print("math.sqrt(7) : ", math.sqrt(7))
print("math.sqrt(math.pi) : ", math.sqrt(math.pi))
