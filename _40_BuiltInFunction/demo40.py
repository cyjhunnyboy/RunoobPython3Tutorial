'''
Python float() 函数
描述：
    float() 函数用于将整数和字符串转换成浮点数
语法：
    class float([x])
    参数：
        x -- 整数或字符串
返回值：
    返回浮点数
'''
if __name__ == "__main__":
    print(float(1))
    print(float(112))
    print(float(-123.6))
    # 字符串
    print(float("123"))
