'''
Python3 len()方法
描述：
    Python len() 方法返回对象（字符、列表、元组等）长度或项目个数
语法：
    len( s )
    参数：
        s -- 对象
返回值：
    返回对象长度
'''
if __name__ == "__main__":
    str1 = "runoob"
    # 字符串长度
    print(len(str1))
