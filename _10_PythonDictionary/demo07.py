"""
字典内置函数&方法
Python字典包含了以下内置函数
"""
# len(dict)计算字典元素个数，即键的总数。
dict_aa = {'Name': 'Runoob', 'Age': 7, 'Class': 'First'}
print(len(dict_aa))

# str(dict) 输出字典，以可打印的字符串表示。
print(str(dict_aa))

# type(variable) 返回输入的变量类型，如果变量是字典就返回字典类型。
print(type(dict_aa))


