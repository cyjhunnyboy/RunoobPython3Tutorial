"""
    Python3 条件控制
       if中常用的操作运算符
           <：小于
           <=：小于或等于
           >：大于
           >=：大于或等于
           ==：等于，比较两个值是否相等
           !=：不等于
"""
if __name__ == "__main__":
    # 程序演示了==操作符
    # 使用数字
    print(5 == 6)
    # 使用变量
    x = 5
    y = 6
    print(x == y)
    print(x < y)
    print(x > y)
    print(x <= y)
    print(x >= y)
    print(x != y)
