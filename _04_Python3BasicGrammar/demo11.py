'''
    Python3 基础语法
        2、字符串(String)
            使用反斜杠(\)+n转义特殊字符
            在字符串前面添加一个r，表示原始字符串，不会发生转义
'''
if __name__ == "__main__":
    # 使用反斜杠(\)+n转义特殊字符
    print("hello\nrunoob")
    # 在字符串前面添加一个r，表示原始字符串，不会发生转义
    print(r"hello\nrunoob")
